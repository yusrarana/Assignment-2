void main(){
   int percentage = 88;
  if (percentage >= 80) {
    print("Grade: A+");
  } else if (percentage >= 70) {
    print("Grade: A");
  } else if (percentage >= 60) {
    print("Grade: B");
  } else if (percentage >= 50) {
    print("Grade: C");
  } else if (percentage >= 40) {
    print("Grade: D");
  } else {
    print("Grade: Fail");
  }
}